<p><img alt="Hot" src="../public/images/konten/HRC.jpg" style="float:left; height:188px; width:240px" />Baja lembaran panas yang berupa coil dan pelat adalah jenis produk baja yang dihasilkan dari proses pengerolan panas. Pabrikan dan para pengguna jenis baja ini umumnya menyebut produk ini &#39;baja hitam&#39; sebagai pembeda terhadap produk baja lembaran dingin yang juga biasa dikenal sebagai &#39;baja putih&#39;.</p>

<p>Krakatau Steel juga memproduksi baja plain carbon dan baja micro-alloyed yang dapat digunakan untuk berbagai penggunaan, dari kualitas umum atau komersial hingga kualitas khusus, seperti struktur rangka baja, komponen dan rangka kendaraan bermotor, tiang pancang, komponen alat berat, fabrikasi umum, pipa dan tabung umum, pipa dan tabung untuk jalur pipa dan casing, tabung gas, baja tahan korosi cuaca, bejana bertekanan, boilers, dan konstruksi kapal.</p>

<p>Ketebalan pelat baja lembaran panas berkisar antara 0,18 hingga 25 mm, sedangkan lebarnya antara 600 hingga 2060 mm. Produk baja lembaran panas dapat diberikan dalam bentuk coil dan pelat. Kondisinya dapat berupa gulungan atau sebagai produk yang melalui proses pickling dan oiling (hot rolled coil-pickled oiled&Acirc;&nbsp; atau HRC-PO).</p>

<p>PT. Krakatau Steel (Persero) Tbk mampu menghasilkan baja lembaran panas berkualitas tinggi untuk penggunaan khusus karena telah menjalankan proses kontrol thermomekanik dan proses desulfurisasi menggunakan ladle furnace.</p>

<p>Penggunaan baja lembaran panas meliputi aplikasi-aplikasi seperti yang tercantum di bawah ini:</p>

<p><img alt="Hot Rolled Coil" src="../public/images/konten/jenis_hrc.jpg" style="float:right; height:180px; width:240px" /></p>

