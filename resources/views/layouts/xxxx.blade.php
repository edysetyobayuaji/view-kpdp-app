	<li>Komponen &amp; Rangka Otomotif</li>
	<li>Jalur Pipa untuk Minyak &amp; Gas</li>
	<li>Casing &amp; Tubing Pipa Sumur Minyak</li>
	<li>Tabung Gas</li>
	<li>Baja Tahan Korosi Cuaca</li>
	<li>Rerolling</li>
